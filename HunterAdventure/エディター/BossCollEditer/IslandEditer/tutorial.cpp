#include "manager.h"
#include "tutorial.h"
#include "fade.h"
#include "renderer.h"

// マクロ定義
#define TRANS_COUNT				(120)									// 遷移までのカウント数

//=========================================
// コンストラクタ
//=========================================
CTutorial::CTutorial()
{
	// 全ての情報をクリアする
	m_nEndCount = 0;			// 終了までのカウント
}

//=========================================
// デストラクタ
//=========================================
CTutorial::~CTutorial()
{

}

//=========================================
//初期化処理
//=========================================
HRESULT CTutorial::Init(void)
{
	// シーンの初期化
	CScene::Init();

	// 全ての値をクリアする
	m_nEndCount = 0;			// 終了までのカウント

	// 成功を返す
	return S_OK;
}

//=============================================
//終了処理
//=============================================
void CTutorial::Uninit(void)
{
	// 終了処理
	CScene::Uninit();
}

//======================================
//更新処理
//======================================
void CTutorial::Update(void)
{
	// 終了カウントを加算する
	m_nEndCount++;

	if (m_nEndCount >= TRANS_COUNT)
	{ // スキップ時または、終了時の場合

		// ゲームモードにする
		CManager::Get()->GetFade()->SetFade(MODE_GAME);
	}

	if (CManager::Get()->GetRenderer() != nullptr)
	{ // レンダラーが NULL じゃない場合

		// 更新処理
		CManager::Get()->GetRenderer()->Update();
	}
}

