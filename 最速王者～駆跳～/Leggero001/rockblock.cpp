CRockBlock::CRockBlock() : CBlock(CObject::TYPE_BLOCK, CObject::PRIORITY_BLOCK)
{

}

//==============================
// デストラクタ
//==============================
CRockBlock::~CRockBlock()
{

}

//==============================
//ブロックの初期化処理
//==============================
HRESULT CRockBlock::Init(void)
{
	// ファイルの設定処理
	SetFileData(CXFile::TYPE_BLOCK_ROCK);

	if (FAILED(CBlock::Init()))
	{ // 初期化処理に失敗した場合

		// 失敗を返す
		return E_FAIL;
	}

	// 値を返す
	return S_OK;
}

//========================================
//ブロックの終了処理
//========================================
void CRockBlock::Uninit(void)
{
	// 終了処理
	CBlock::Uninit();
}

